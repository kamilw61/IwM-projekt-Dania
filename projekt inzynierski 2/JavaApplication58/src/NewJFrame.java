    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        String filePath = "C:\\Users\\adria\\Desktop\\rable.txt";
        File file = new File(filePath);

        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            // get the first line
            // get the columns name from the first line
            // set columns name to the jtable model
            String firstLine = br.readLine().trim();
            String[] columnsName = firstLine.split(",");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setColumnIdentifiers(columnsName);

            // get lines from txt file
            Object[] tableLines = br.lines().toArray();

            // extratct data from lines
            // set data to jtable model
            for (int i = 0; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split("/");
                model.addRow(dataRow);
            }

        } catch (Exception ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed

        try {
            int a[], l, p, s, n, x;

            //liczba elementÃ³w tablicy, wylicza ilosc wierszy
            n = jTable1.getRowCount();
            a = new int[n];
            //pobiera wartosci z danego wiersza w pierwszej kolumnie
            for (int i = 0; i < n; i++) {

                a[i] = Integer.parseInt((String) jTable1.getValueAt(i, 0));

//            int z = ((Integer)jTable1.getValueAt(i, 1)).intValue();
//            a[i]=z;
            }

            //element do wyszukania
            x = Integer.parseInt(jTextField1.getText());

            //szukaj elementu x
            l = 0;
            p = n - 1;
            while (l <= p) {
                s = (l + p) / 2;

                if (a[s] == x) {

                    //podaj wynik
                    StringBuilder sb = new StringBuilder();
                    sb.append(x);
                    String str = sb.toString();
                    jTextField2.setText(str);
                    //zakoncz program
                    return;
                }

                if (a[s] < x) {
                    l = s + 1;
                } else {
                    p = s - 1;
                }
            }
        } catch (Exception ex) {
            System.out.println(ex);          
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

